function useCounter(end: number, duration: number = 3500, inView: boolean) {
  const [count, setCount] = useState(0);

  useEffect(() => {
    if (!inView) return;

    let startTime: number | null = null;
    let animationFrame: number;

    const animate = (timestamp: number) => {
      if (!startTime) startTime = timestamp;
      const progress = Math.min((timestamp - startTime) / duration, 1);
      const eased = 1 - Math.pow(1 - progress, 3);
      setCount(Math.floor(eased * end));

      if (progress < 1) {
        animationFrame = requestAnimationFrame(animate);
      }
    };

    animationFrame = requestAnimationFrame(animate);
    return () => cancelAnimationFrame(animationFrame);
  }, [end, duration, inView]);

  return count;
}

function StatCard({ stat, inView }: { stat: StatItem; inView: boolean }) {
  const count = useCounter(stat.value, 3500, inView);

  return (
    <div className="stat-item">
      <div className="stat-icon">{stat.icon}</div>
      <div className="stat-number-wrapper">
        <span className="stat-number">{count}</span>
        {stat.suffix && <span className="stat-suffix">{stat.suffix}</span>}
      </div>
      <span className="stat-label">{stat.label}</span>
    </div>
  );
}

export default function StatsRow() {
  const ref = useRef<HTMLDivElement>(null);
  const [inView, setInView] = useState(false);

  useEffect(() => {
    const el = ref.current;
    if (!el) return;

    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setInView(true);
          observer.disconnect();
        }
      },
      { threshold: 0.3 }
    );

    observer.observe(el);
    return () => observer.disconnect();
  }, []);

  return (
    <div className="about-stats" ref={ref}>
      {STATS.map((stat) => (
        <StatCard key={stat.label} stat={stat} inView={inView} />
      ))}
    </div>
  );
}
